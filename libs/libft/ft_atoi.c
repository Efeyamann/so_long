int	ft_atoi(const char *str)
{
	int	b;
	int	i;
	int	a;

	i = 0;
	a = 1;
	b = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	if (str[i] == '-')
	{
		a = -1;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b = (b * 10) + (str[i] - '0');
		i++;
	}
	return (a * b);
}
